class Data{
  String nama;
  String link;
  String logo;
  String gambar;
  String bunga;
  String nominal;
  String buku;

  Data(String nama, String link, String logo, String gambar, String bunga, String nominal, String buku){
    this.nama = nama;
    this.link = link;
    this.logo = logo;
    this.gambar = gambar;
    this.bunga = bunga;
    this.nominal = nominal;
    this.buku = buku;
  }
}
